static inline void SX1262_WaitWhileBusy(void)
{
    /* BUSY = HIGH → radio is busy */
    /* BUSY = LOW  → radio is ready */

/* VERY IMPORTANT */

    while (HAL_GPIO_ReadPin(BUSY_GPIO_Port, BUSY_Pin) == GPIO_PIN_SET)
    {
        /* optional: add small delay to reduce CPU load */
        __NOP();
    }
}

static void SX1262_Reset(void)
{
    /* Hold reset low */
    HAL_GPIO_WritePin(RST_GPIO_Port, RST_Pin, GPIO_PIN_RESET);
    HAL_Delay(2);                  // >10 µs required, 2 ms is safe

    /* Release reset */
    HAL_GPIO_WritePin(RST_GPIO_Port, RST_Pin, GPIO_PIN_SET);

    /* Mandatory delay after reset release */
    HAL_Delay(1);                  // VERY IMPORTANT (boot ROM time)

    /* Wait until SX1262 is ready */
    SX1262_WaitWhileBusy();
}

#define SX126X_CMD_SET_STANDBY   0x80
#define SX126X_STANDBY_RC        0x00
#define SX126X_STANDBY_XOSC      0x01
#define SX126X_CMD_READ_REGISTER  0x1D

static void SX1262_SetStandby(uint8_t mode)
{
    uint8_t cmd[2] = { SX126X_CMD_SET_STANDBY, mode };

    SX1262_WaitWhileBusy();
    HAL_GPIO_WritePin(NSS_GPIO_Port, NSS_Pin, GPIO_PIN_RESET);
    HAL_SPI_Transmit(&hspi2, cmd, 2, HAL_MAX_DELAY);
    HAL_GPIO_WritePin(NSS_GPIO_Port, NSS_Pin, GPIO_PIN_SET);
    SX1262_WaitWhileBusy();
}


#define SX126X_CMD_READ_REGISTER  0x1D

uint8_t SX1262_ReadRegister(uint16_t address)
{
    SX1262_SetStandby(SX126X_STANDBY_RC);
    static uint8_t txBuf[5];
    static uint8_t rxBuf[5];

    txBuf[0] = SX126X_CMD_READ_REGISTER;
    txBuf[1] = (address >> 8) & 0xFF;
    txBuf[2] = address & 0xFF;
    txBuf[3] = 0x00;   // mandatory dummy
    txBuf[4] = 0x00;   // read happens here

    SX1262_WaitWhileBusy();

    HAL_GPIO_WritePin(NSS_GPIO_Port, NSS_Pin, GPIO_PIN_RESET);
    // HAL_SPI_TransmitReceive(&hspi2, txBuf, rxBuf, 5, HAL_MAX_DELAY);

    if( HAL_SPI_TransmitReceive(&hspi2, txBuf, rxBuf, 5, HAL_MAX_DELAY) != HAL_OK)
    {
        Error_Handler();
    }
    HAL_GPIO_WritePin(NSS_GPIO_Port, NSS_Pin, GPIO_PIN_SET);

    SX1262_WaitWhileBusy();

    return rxBuf[4];   // ONLY valid byte
}

uint8_t SX1262_GetStatus(void)
{
    uint8_t tx[3] = { 0x1D,0xC0, 0x00 };
    uint8_t rx[3];

    SX1262_WaitWhileBusy();
    HAL_GPIO_WritePin(NSS_GPIO_Port, NSS_Pin, GPIO_PIN_RESET);
    // HAL_SPI_TransmitReceive(&hspi2, tx, rx, 2, HAL_MAX_DELAY);
    if( HAL_SPI_TransmitReceive(&hspi2, tx, rx, 2, HAL_MAX_DELAY) != HAL_OK)
    {
        Error_Handler();
    }
    HAL_GPIO_WritePin(NSS_GPIO_Port, NSS_Pin, GPIO_PIN_SET);
    SX1262_WaitWhileBusy();

    return rx[2];
}

