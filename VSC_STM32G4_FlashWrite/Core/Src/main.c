HAL_StatusTypeDef Flash_Write(uint32_t address, uint32_t *data, uint32_t len)
{
    HAL_StatusTypeDef status;
    uint32_t pageError = 0;

    FLASH_EraseInitTypeDef eraseInit;

    HAL_FLASH_Unlock();

    /* 1. Erase Flash page */
    eraseInit.TypeErase   = FLASH_TYPEERASE_PAGES;
    eraseInit.Page        = (address - FLASH_BASE) / FLASH_PAGE_SIZE;
    eraseInit.NbPages     = 1;

    status = HAL_FLASHEx_Erase(&eraseInit, &pageError);
    if (status != HAL_OK)
    {
        HAL_FLASH_Lock();
        return status;
    }

    /* 2. Program Flash */
    for (uint32_t i = 0; i < len; i++)
    {
        status = HAL_FLASH_Program(
                    FLASH_TYPEPROGRAM_DOUBLEWORD,   // G4 uses double word
                    address,
                    data[i]);

        if (status != HAL_OK)
        {
            HAL_FLASH_Lock();
            return status;
        }

        address += 8; // double-word = 8 bytes
    }

    HAL_FLASH_Lock();
    return HAL_OK;
}

